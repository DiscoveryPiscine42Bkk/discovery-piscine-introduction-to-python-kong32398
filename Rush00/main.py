    print("-" * 30)
    for category, count in category_count.items():
        print(f"   {category}: {count} ‡∏á‡∏≤‡∏ô")
    
    print("\n‚≠ê ‡∏™‡∏£‡∏∏‡∏õ‡∏ï‡∏≤‡∏°‡∏Ñ‡∏ß‡∏≤‡∏°‡∏™‡∏≥‡∏Ñ‡∏±‡∏ç:")
    print("-" * 30)
    for priority, count in priority_count.items():
        if count > 0:
            print(f"   {priority}: {count} ‡∏á‡∏≤‡∏ô")

