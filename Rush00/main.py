    if not task_list:
        print("üìã ‡∏¢‡∏±‡∏á‡πÑ‡∏°‡πà‡∏°‡∏µ‡∏á‡∏≤‡∏ô‡πÉ‡∏ô‡∏ü‡∏≤‡∏£‡πå‡∏°")
        return
    
    # ‡∏ô‡∏±‡∏ö‡∏à‡∏≥‡∏ô‡∏ß‡∏ô‡∏á‡∏≤‡∏ô‡πÉ‡∏ô‡πÅ‡∏ï‡πà‡∏•‡∏∞‡∏õ‡∏£‡∏∞‡πÄ‡∏†‡∏ó
    category_count = {}
    priority_count = {'‡∏™‡∏π‡∏á': 0, '‡∏Å‡∏•‡∏≤‡∏á': 0, '‡∏ï‡πà‡∏≥': 0}
    
    for task in task_list:
        # ‡∏ô‡∏±‡∏ö‡∏ï‡∏≤‡∏°‡∏õ‡∏£‡∏∞‡πÄ‡∏†‡∏ó
        category = task['category']
        if category in category_count:
            category_count[category] += 1
        else:
            category_count[category] = 1
        
        # ‡∏ô‡∏±‡∏ö‡∏ï‡∏≤‡∏°‡∏Ñ‡∏ß‡∏≤‡∏°‡∏™‡∏≥‡∏Ñ‡∏±‡∏ç
        priority = task['priority']
        if priority in priority_count:
            priority_count[priority] += 1
    
    print(f"üìä ‡∏à‡∏≥‡∏ô‡∏ß‡∏ô‡∏á‡∏≤‡∏ô‡∏ó‡∏±‡πâ‡∏á‡∏´‡∏°‡∏î: {len(task_list)} ‡∏á‡∏≤‡∏ô\n")
    
